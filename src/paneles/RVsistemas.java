    protected void ingresar(){
      int usuario_cedula = Integer.valueOf(ingresar_cedula.getText());
    String clave = String.valueOf(ingresar_password.getText());
    
    validar_usuario validando = new validar_usuario();
    usuario usua = new usuario();
    usua.setCedula(usuario_cedula);
    usua.setPassword(clave);
    us = validando.obtenerusuario(usua);
    
    if (us != null) {
        contador = 0;
        int rol_usuario = us.getId_rol();
        
        id_usuario = us.getCedula(); 
        Interfaz inter = new Interfaz(rol_usuario);
        
        inter.setVisible(true);
        this.dispose();
        this.ingresar_cedula.setText("");
        this.ingresar_password.setText("");
        
    } else {
        contador++;
        JOptionPane.showConfirmDialog(null, "Datos invalidos, Error");
        if (contador >= 4) {
            JOptionPane.showConfirmDialog(null, "demasiados Intentos por seguridad El Sistema se Cerrara");
            System.exit(0);
        }
    }
        
    }
