    private void ojo_cambiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ojo_cambiaMouseClicked
        // TODO add your handling code here:
          if (renovando.getEchoChar() == (char) 0|| renovando_confi.getEchoChar()== (char) 0) {
        renovando.setEchoChar('*'); 
        renovando_confi.setEchoChar('*'); 
        ojo_cambia.setIcon(new ImageIcon(getClass().getResource("/imagen/ojo_cerrado.png"))); 
    } else {
        renovando.setEchoChar((char) 0); 
        renovando_confi.setEchoChar((char) 0);
        ojo_cambia.setIcon(new ImageIcon(getClass().getResource("/imagen/ojo_abierto.png"))); 
    }
    }//GEN-LAST:event_ojo_cambiaMouseClicked

