    private void jButton_confir2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_confir2ActionPerformed
          boolean valido = validar_campos.validarPasswords(
            renovando, renovando_confi, jLabel_1contra, jLabel_2contra);

    if (valido) {
        String reno = new String(renovando.getPassword());
        String confir = new String(renovando_confi.getPassword());

        if (reno.equals(confir)) {
            usuario us = new usuario();
            us.setCedula(dato);  
            us.setPassword(reno);
            us.setConfi_password(confir);
            us.update();

            JOptionPane.showMessageDialog(this, "Contraseña Actualizada");

            renovando.setText("");
            renovando_confi.setText("");

            RVsistemas pm = new RVsistemas();
            pm.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden");
        }
    }
        
     
    }//GEN-LAST:event_jButton_confir2ActionPerformed

