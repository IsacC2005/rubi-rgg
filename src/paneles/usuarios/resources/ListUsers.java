    public void cargarTablaDeUsuarios() {

        usuarios = usuario_model.getAllUsers();

        String[] columnNames = usuarios.get(0);

        List<String[]> dataList = usuarios.subList(1, usuarios.size());

        Object[][] data = new Object[dataList.size()][columnNames.length];

        for (int i = 0; i < dataList.size(); i++) {
            data[i] = dataList.get(i);
        }

        DefaultTableModel model = new DefaultTableModel(data, columnNames);

        jTable1.setModel(model);
    }

